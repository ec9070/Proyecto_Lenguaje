    public void conectar() {
        try {
            Class.forName("oracle.jdbc.driver.OracleDriver");
            String url = "jdbc:oracle:thin:@localhost:1521:orcl";
            String user = "tienda";
            String password = "tienda01";
            try {
                con = DriverManager.getConnection(url, user, password);
            } catch (SQLException ex) {
                Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public boolean existe(String id) {
        int aux = 0;
        try {
            conectar();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select paquete_clientes.repetido('" + id + "') from dual");
            while (rs.next()) {
                aux = rs.getInt(1);
            }
            con.close();
            if (aux == 1) {
                return true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

    public void buscar() {
        String id;
        Cliente c;
        id = JOptionPane.showInputDialog(null, "Digite el id del cliente que desea buscar: ");
        try {
            while (!(id.length() == 9)) {
                id = JOptionPane.showInputDialog(null, "La cedula debe tener 9 digitos vuelva a ingresarla: ");
            }
            if (!existe(id)) {
                JOptionPane.showMessageDialog(null, "Â¡El cliente no existe!");
                this.dispose();
            } else {
                c = llenar(id);
                jTextField7.setText(c.getId_cliente());
                jTextField5.setText(c.getNombre());
                jTextField6.setText(c.getApellido());
                jTextField9.setText(c.getCorreo());
                jTextField8.setText(c.getTelefono());
                jTextField7.setEditable(false);
                jTextField5.setEditable(false);
                jTextField6.setEditable(false);
                jTextField9.setEditable(false);
                jTextField8.setEditable(false);
            }
        }catch(NullPointerException ex){
            this.dispose();
        } 
    }

    public Cliente llenar(String id) {
        Cliente c = new Cliente();
        try {
            conectar();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from Cliente where id_cliente='" + id + "'");
            while (rs.next()) {
                c.setId_cliente(rs.getString(1));
                c.setNombre(rs.getString(2));
                c.setApellido(rs.getString(3));
                c.setCorreo(rs.getString(4));
                c.setTelefono(rs.getString(5));
                c.setId_credito(rs.getInt(6));
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        return c;
    }

