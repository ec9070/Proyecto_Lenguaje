public class Persona {

    protected String primer_apellido;
    protected String segundo_apellido;
    protected String nombre;
    protected String correo;
    protected String direccion;

    public Persona() {
        this.primer_apellido = "";
        this.segundo_apellido = "";
        this.nombre = "";
        this.correo = "";
        this.direccion = "";
    }
}
